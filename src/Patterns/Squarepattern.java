package Patterns;

public class Squarepattern {
    public static void main(String args[])
    {
        for(int i=1;i<=6;i++)
        {
            if(i==1 || i==6)
            {
                System.out.println("------");
            }
            else
            {
                System.out.println("|    |");
            }

        }

    }
}
